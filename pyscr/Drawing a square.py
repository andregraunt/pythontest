#Example 2: Drawing a square.
#Lines are boring. We can rotate the turtle in order to draw more interesting figures.

#Code
import turtle

silly = turtle.Turtle()

silly.forward(50)
silly.right(90)     # Rotate clockwise by 90 degrees

silly.forward(50)
silly.right(90)

silly.forward(50)
silly.right(90)

silly.forward(50)

silly.right(90)

turtle.done()
